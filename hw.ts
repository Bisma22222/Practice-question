const age = 20;  
if(age>=6 && age<=12){
    console.log("You are eligible for a Child ticket")
}
else if(age>12 && age<=20){
    console.log("You are eligible for Teenager ticket")
}
else if(age>20 && age<=40){
    console.log("You are eligible for Adult ticket")
}
else if(age>40 && age<=60){
    console.log("You are eligible for old-people ticket")
}
else{
    console.log("You are eligible for senior citizen ticket")

}
